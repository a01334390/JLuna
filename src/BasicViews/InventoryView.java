    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        String ntype = search.getText();
        currentNotebook = DatabaseManager.searchNotebookByType(ntype);
        type.setText(currentNotebook.getType());
        id_Notebook.setText(Integer.toString(currentNotebook.getId()));
        currentInventory = DatabaseManager.getInventoryByNotebookID(currentNotebook.getId());
        ammount.setText(Integer.toString(currentInventory.getAmmount()));
        if (currentNotebook == null) {
            JOptionPane.showMessageDialog(null, "Hubo problemas con el cuaderno", "Cuaderno", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_searchActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       int idNotebook = currentInventory.getId_Notebook();
       int ammounty = Integer.parseInt(ammount.getText());
       if(DatabaseManager.editInventoryInDatabase(idNotebook,ammounty)){
           JOptionPane.showMessageDialog(null, "Se actualizo correctamente el inventario", "Inventario", JOptionPane.INFORMATION_MESSAGE);
       }else{
           JOptionPane.showMessageDialog(null, "No se actualizo correctamente el inventario", "Inventario", JOptionPane.WARNING_MESSAGE);
       }
       type.setText("");
       id_Notebook.setText("");
       ammount.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed

